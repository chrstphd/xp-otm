package otm.shard;

public class ShardPixelCoordinates {
    private final int x;
    private final int y;

    public ShardPixelCoordinates(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }


}